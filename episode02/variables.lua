-- Episode 2: Variables and Types

-- Strings hold text
local name = "Alice"
print(name)
print(type(name))

-- Numbers hold integers and decimals
local age = 25
print(age)
print(type(age))

-- Booleans hold true or false
local is_student = true
print(is_student)
print(type(is_student))

-- Nil means "no value"
local nickname = nil
print(nickname)
print(type(nickname))

-- Summary
print("--- Summary ---")
print(name .. " is " .. age .. " years old")
